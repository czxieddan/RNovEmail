impl Lang {
    pub fn parse(value: Option<&str>) -> Self {
        match value {
            Some(value) if value.eq_ignore_ascii_case("ja") => Self::Ja,
            _ => Self::Zh,
        }
    }

    pub fn code(self) -> &'static str {
        match self {
            Self::Zh => "zh",
            Self::Ja => "ja",
        }
    }
}

pub fn text(lang: Lang, key: Text) -> &'static str {
    match lang {
        Lang::Zh => zh(key),
        Lang::Ja => ja(key),
    }
}

