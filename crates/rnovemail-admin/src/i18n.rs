impl Lang {
    pub fn parse(value: Option<&str>) -> Self {
        match value {
            Some(value) if value.eq_ignore_ascii_case("ja") => Self::Ja,
            Some(value) if value.eq_ignore_ascii_case("en") => Self::En,
            Some(value) if value.eq_ignore_ascii_case("ru") => Self::Ru,
            _ => Self::Zh,
        }
    }

    pub fn code(self) -> &'static str {
        match self {
            Self::Zh => "zh",
            Self::Ja => "ja",
            Self::En => "en",
            Self::Ru => "ru",
        }
    }
}

pub fn text(lang: Lang, key: Text) -> &'static str {
    match lang {
        Lang::Zh => zh(key),
        Lang::Ja => ja(key),
        Lang::En => en(key),
        Lang::Ru => ru(key),
    }
}

